#include "libft.h"

int		ft_atoi(const char *str)
{
	int		i;
	long	number;
	int		sign;
	int		tmp;

	i = 0;
	number = 0;
	sign = 1;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n' || str[i] == '\r'
			|| str[i] == '\v' || str[i] == '\f')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		tmp = number;
		number = number * 10 + (str[i++] - '0');
		if (tmp && (tmp ^ number) < 0)
			return ((sign == 1) ? -1 : 0);
	}
	return ((int)number * sign);
}
